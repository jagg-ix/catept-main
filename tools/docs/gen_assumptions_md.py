#!/usr/bin/env python3
"""Generate docs/architecture/ASSUMPTIONS.md from the CATEPTAssumption registry.

Inputs:
  CATEPTMain/Core/Assumptions.lean — the registry (AssumptionId namespace)
  *.lean                            — every Lean file in the tree (excluding .lake/)

Outputs:
  docs/architecture/ASSUMPTIONS.md  — per-id reference counts, dead list, totals.

Run from repo root:
  python3 tools/docs/gen_assumptions_md.py

Idempotent: re-running produces no diff if nothing changed.
"""
from __future__ import annotations

import re
from collections import Counter
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
REGISTRY = ROOT / "CATEPTMain" / "Core" / "Assumptions.lean"
OUT      = ROOT / "docs" / "architecture" / "ASSUMPTIONS.md"

# Match `def <name> : String := "<payload>"` inside the AssumptionId namespace.
ID_DEF_RE = re.compile(r'^def\s+(\w+)\s*:\s*String\s*:=\s*"([^"]+)"', re.MULTILINE)

# Match `CATEPTAssumption AssumptionId.<name>` or `CATEPTAssumption <name>`
# (the shorter form is reachable via `open CATEPTMain.AssumptionId`).
CALL_RE = re.compile(r'CATEPTAssumption\s+(?:AssumptionId\.)?(\w+)\b')

# Match preceding docstring lines (the /-- ... -/ block before each def).
DOC_AND_DEF_RE = re.compile(
    r'/--\s*(.*?)\s*-/\s*\n\s*def\s+(\w+)\s*:\s*String\s*:=\s*"([^"]+)"',
    re.DOTALL,
)


def parse_registry() -> dict[str, tuple[str, str]]:
    """Return {const_name: (payload, docstring)}."""
    text = REGISTRY.read_text()
    entries: dict[str, tuple[str, str]] = {}

    # First, collect entries with docstrings.
    for m in DOC_AND_DEF_RE.finditer(text):
        doc = " ".join(line.strip() for line in m.group(1).splitlines()).strip()
        entries[m.group(2)] = (m.group(3), doc)

    # Then, catch any defs without a preceding docstring.
    for m in ID_DEF_RE.finditer(text):
        if m.group(1) not in entries:
            entries[m.group(1)] = (m.group(2), "")

    return entries


def count_refs(registered: set[str]) -> Counter[str]:
    refs: Counter[str] = Counter()
    for p in ROOT.rglob("*.lean"):
        if ".lake" in p.parts or p == REGISTRY:
            continue
        try:
            for m in CALL_RE.finditer(p.read_text()):
                name = m.group(1)
                if name in registered:
                    refs[name] += 1
        except UnicodeDecodeError:
            pass
    return refs


def domain_of(payload: str) -> str:
    return payload.split(".", 1)[0] if "." in payload else "(unknown)"


def main() -> None:
    entries = parse_registry()
    if not entries:
        raise SystemExit(f"No AssumptionId entries found in {REGISTRY}")

    refs = count_refs(set(entries))
    total = len(entries)
    referenced = sum(1 for n in entries if refs[n] > 0)
    dead = [n for n in entries if refs[n] == 0]

    lines = [
        "# CATEPT Assumption Registry",
        "",
        "_Auto-generated by `tools/docs/gen_assumptions_md.py`. Do not hand-edit._",
        "",
        "## Summary",
        "",
        f"- **Registered ids**: {total}",
        f"- **Referenced in source**: {referenced}",
        f"- **Dead (zero references)**: {len(dead)}",
        "",
        "## By domain",
        "",
    ]

    by_domain: dict[str, list[str]] = {}
    for name, (payload, _doc) in entries.items():
        by_domain.setdefault(domain_of(payload), []).append(name)

    lines.append("| Domain | Ids | Referenced |")
    lines.append("|---|---:|---:|")
    for domain in sorted(by_domain):
        ids = by_domain[domain]
        ref_count = sum(1 for n in ids if refs[n] > 0)
        lines.append(f"| `{domain}.*` | {len(ids)} | {ref_count} |")

    lines.append("")
    lines.append("## Full registry")
    lines.append("")
    lines.append("| AssumptionId (Lean) | Payload (string id) | Refs | Description |")
    lines.append("|---|---|---:|---|")
    for name in sorted(entries):
        payload, doc = entries[name]
        doc_cell = doc.replace("|", r"\|") if doc else "—"
        lines.append(f"| `{name}` | `{payload}` | {refs[name]} | {doc_cell} |")

    if dead:
        lines.append("")
        lines.append("## Dead assumptions (zero references)")
        lines.append("")
        lines.append(
            "These ids are registered but not referenced from any source file. "
            "Candidates for retirement, or retrofit sites for a future pass."
        )
        lines.append("")
        for name in sorted(dead):
            payload = entries[name][0]
            lines.append(f"- `{name}` → `{payload}`")
        lines.append("")

    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text("\n".join(lines) + "\n")
    print(
        f"Wrote {OUT.relative_to(ROOT)} "
        f"({total} registered, {referenced} referenced, {len(dead)} dead, "
        f"{sum(refs.values())} total refs)"
    )


if __name__ == "__main__":
    main()
